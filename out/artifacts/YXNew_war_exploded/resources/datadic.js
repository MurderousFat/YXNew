var _global_datadic_extfunc = {};
var _global_datadic_store = {};
		

function g_getExtDatadic(dicName){
	if(!_global_datadic_extfunc[dicName]){
		var _datadic = _global_datadic[dicName];
		if(_datadic){
			_global_datadic_extfunc[dicName] = function(_value){
				for(var i in _datadic){
					if(_value===_datadic[i].code)return '<span style="color:'+(_datadic[i].color || '#000000')+'">'+_datadic[i].codedesc+'</span>';
				}
				return _value;
			}
		}else{
			_global_datadic_extfunc[dicName] = function(_value){
				return _value;
			}
		}
	}
	return _global_datadic_extfunc[dicName];
}

function g_getDatadic(dicName){
	return _global_datadic[dicName] || [];
}

function g_getDatadicText(dicName,code){
	var _items = _global_datadic[dicName] || [];
	var _item;
	for(var i in _items){
		if(code==_items[i].code){
			_item = _items[i].codedesc;
		}
	}
	return _item || '';
}

function g_getDatadicStore(dicName){
	if(!_global_datadic_store[dicName]){
		_global_datadic_store[dicName] = Ext.create('Ext.data.Store',{model:'Datadic',data:g_getDatadic(dicName)});
	}
	return _global_datadic_store[dicName];
}

